// 创建合约实例
const contract = new ethers.Contract(CONTRACT_ADDRESS, contractABI, wallet);

async function mintNFT(to, tokenURI) {
  if (!MINTER_PRIVATE_KEY || !BSC_TESTNET_RPC_URL || !CONTRACT_ADDRESS) {
    throw new Error("Missing blockchain environment variables.");
  }
  
  console.log(`-> Minting NFT for ${to} with URI ${tokenURI}...`);

  // 调用合约的 safeMint 函数
  const tx = await contract.safeMint(to, tokenURI);
  
  console.log("Transaction sent. Waiting for confirmation...", tx.hash);
  
  // 等待交易被区块链确认
  const receipt = await tx.wait();

  console.log("<- NFT minted successfully! Transaction confirmed in block:", receipt.blockNumber);
  
  // 从交易回执中解析出 TokenId
  // 注意：这取决于你的合约事件。ERC721 `Transfer` 事件的第三个参数是 tokenId。
  const event = receipt.events.find(event => event.event === 'Transfer');
  const tokenId = event.args.tokenId.toString();

  return { txHash: receipt.transactionHash, tokenId: tokenId };
}

module.exports = { mintNFT };